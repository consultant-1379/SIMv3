    public static class RenameLocalFile implements Process<String, String> {

        private final Config configuration;
        private final Matcher remoteFilePatternMatcher;

        public RenameLocalFile(Config configuration, Matcher remoteFilePatternMatcher) {
            this.configuration = configuration;
            this.remoteFilePatternMatcher = remoteFilePatternMatcher;
        }

        @Override
        public String process(String localFileReplacePattern) {
            String updatedFileName = remoteFilePatternMatcher.replaceFirst(localFileReplacePattern);
            updatedFileName = updatedFileName.replaceAll(Constants.PATTERN_REPLACE_IPADDRESS, configuration.getPropertyAsString(Constants.CONFIG_IPADDRESS, ""));
            updatedFileName = updatedFileName.replaceAll(Constants.PATTERN_REPLACE_HASHCODE, "" + uniqueHashcode());

            return updatedFileName;
        }
    }

